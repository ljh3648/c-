#include <iostream>
using namespace std;

int main()
{
    int a = 8;
    int b = 4;

    int c = a + b;
    int d = a - b;
    int e = a * b;
    int f = a / b;

    int g = a % b;

    cout << "8 + 4 = " << c << "\n";
    cout << "8 - 4 = " << d << "\n";
    cout << "8 * 4 = " << e << "\n";
    cout << "8 / 4 = " << f << "\n";
    cout << "8 % 4 = " << g << "\n";

    return 0;
}
